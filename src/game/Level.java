	public void printCurrentLevelToConsole() {
		char[][] currentMap = blueprints.get(currentLevelIndex);

		for (int row = 0; row < currentMap.length; row++) {
			for (int col = 0; col < currentMap[row].length; col++) {
				System.out.print(currentMap[row][col] + " ");
			}
			System.out.println();
		}

	}

	public void nextLevel() {
		currentLevelIndex++; // Move to next level index
	}

	/**
	 * Loads the level at the current index to be drawn on the screen
	 */
	public void loadCurrentLevel() {
		activeTiles.clear();
		char[][] map = blueprints.get(currentLevelIndex);

		for (int row = 0; row < map.length; row++) {
			for (int col = 0; col < map[row].length; col++) {
				char symbol = map[row][col];
				// x & y values for each tile
				int xPixel = col * 40;
				int yPixel = row * 40;

				if (symbol == 'W') {
					activeTiles.add(new Wall(xPixel, yPixel, 40, 40));
				} else if (symbol == 'P') {
					activeTiles.add(new Platform(xPixel, yPixel, 40, 40));
				} else if (symbol == 'R') {
					activeTiles.add(new Gem(xPixel, yPixel, 40, 40, "Purple"));
				} else if (symbol == 'G') {
					activeTiles.add(new Gem(xPixel, yPixel, 40, 40, "Green"));
				} else if (symbol == 'D') {
					activeTiles.add(new Door(xPixel, yPixel, 40, 40, "Purple"));
				} else if (symbol == 'E') {
					activeTiles.add(new Door(xPixel, yPixel, 40, 40, "Green"));
				} else if (symbol == 'X') {
					activeTiles.add(new Gas(xPixel, yPixel, 40, 40, "Purple"));
				} else if (symbol == 'Y') {
					activeTiles.add(new Gas(xPixel, yPixel, 40, 40, "Green"));
				} else if (symbol == 'O') {
					activeTiles.add(new Button(xPixel, yPixel, 40, 40));
				} else if (symbol == 'L') {
					activeTiles.add(new Lever(xPixel, yPixel, 40, 40));
				} else if (symbol == '1') {
					player1 = new Character(xPixel, yPixel, 30, 30, "Purple", true);
				} else if (symbol == '2') {
					player2 = new Character(xPixel, yPixel, 30, 30, "Green", false);
				}
			}
		}

	}
